    private void jPanel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel6MouseClicked
        
        
        try {
            
            pst=conn.createStatement();
            
            String uname=username.getText();
            String pass=pw.getText();
            
            if(uname.isEmpty() || pass.isEmpty()){
                
                JOptionPane.showMessageDialog(null,"Empty values");
                
            }else{
            
            String sql="SELECT * FROM register WHERE user_name='"+uname+"' && password='"+pass+"'";
           
            rs=pst.executeQuery(sql);
            
        if(rs.next()){
            
            JOptionPane.showMessageDialog(null, "Loging successfully!");   
                
                Home2 ho=new Home2();
                ho.setVisible(true);
                this.dispose();
        }else{
            
            JOptionPane.showMessageDialog(null, "User Name or Password Invalid");
                
            }
            }
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null,e);
        }
        
        
    }//GEN-LAST:event_jPanel6MouseClicked

